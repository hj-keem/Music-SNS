package com.example.Final_Project_mutso.dto;


import com.example.Final_Project_mutso.entity.PlayListCommentEntity;
import lombok.Data;

@Data
public class PLCommentDto {
    private Long id;
    private String content;

    public static PLCommentDto fromEntity(PlayListCommentEntity entity){
        PLCommentDto dto = new PLCommentDto();

        dto.setId(entity.getId());
        dto.setContent(entity.getContent());
        return dto;
    }
}
